    private void populateOrganizationComboBox()
    {
        organizationJComboBox.removeAllItems();
        //Populate Customer Organizations
        for(Organization o:organizationDirectory.getOrganizationList())
        {   
            if(o.getName().equals(Organization.Type.Customer.getValue()))
            {
                organizationJComboBox.addItem(o);
            }
        }
    }
    
    private void populateComboboxes()
    {
        genderJComboBox.removeAllItems();;
        ageRangeJComboBox.removeAllItems();
        incomeRangeJComboBox.removeAllItems();;
        occupationJComboBox.removeAllItems();
        
        for(Gender gender: Gender.values())
        {
            if(!gender.getValue().equals(Gender.Any.getValue()))
            {
                genderJComboBox.addItem(gender);
            }
        }
        
        for(AgeRange ageRange: AgeRange.values())
        {
            if(!ageRange.getValue().equals(AgeRange.Any.getValue()))
            {
                ageRangeJComboBox.addItem(ageRange);
            }
        }
        
        for(IncomeRange incomeRange: IncomeRange.values())
        {
            if(!incomeRange.getValue().equals(IncomeRange.Any.getValue()))
            {
                incomeRangeJComboBox.addItem(incomeRange);
            }
        }
        
        for(Occupation occupation: Customer.Occupation.values())
        {
            if(!occupation.getValue().equals(Occupation.Any.getValue()))
            {
                occupationJComboBox.addItem(occupation);
            }
        }
    }

