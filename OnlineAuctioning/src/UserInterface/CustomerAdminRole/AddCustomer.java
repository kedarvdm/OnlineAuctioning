    private Boolean validateFields()
    {
        if(genderJComboBox.getSelectedIndex()==-1
            || occupationJComboBox.getSelectedIndex()==-1
                || incomeRangeJComboBox.getSelectedIndex()==-1
                || ageRangeJComboBox.getSelectedIndex()==-1
                || customerNameJTextField.getText().length()==0
                || addressJTextField.getText().length()==0
                || cityJTextField.getText().length()==0
                || stateJTextField.getText().length()==0
                || countryJTextField.getText().length()==0
                || zipJTextField.getText().length()==0
                || emailJTextField.getText().length()==0
                || phoneNumberJTextField.getText().length()==0)
        {
            return false;
        }
        return true;
    }
    private void addCustomerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerJButtonActionPerformed
        // TODO add your handling code here:
        Organization org= (Organization) organizationJComboBox.getSelectedItem();
        
        if(org==null)
        {
            JOptionPane.showMessageDialog(this,"Please select an organization.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!validateFields())
        {
            JOptionPane.showMessageDialog(this,"Please enter correct fields.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        Customer customer=org.getPersonDirectory().createAndAddCustomer();
        customer.setName(customerNameJTextField.getText());
        customer.setAddress1(addressJTextField.getText());
        customer.setCity(cityJTextField.getText());
        customer.setState(stateJTextField.getText());
        customer.setCountry(countryJTextField.getText());
        customer.setZip(zipJTextField.getText());
        
        Gender gender= (Gender) genderJComboBox.getSelectedItem();
        customer.setGender(gender);
        
        AgeRange ageRange=(AgeRange) ageRangeJComboBox.getSelectedItem();
        customer.setAgeRange(ageRange);
        
        IncomeRange incomeRange= (IncomeRange) incomeRangeJComboBox.getSelectedItem();
        customer.setIncomeRange(incomeRange);
        
        Occupation occupation= (Occupation) occupationJComboBox.getSelectedItem();
        customer.setOccupation(occupation);
        
        customer.setCustomerEmail(emailJTextField.getText());
        customer.setPhoneNumber(phoneNumberJTextField.getText());
        JOptionPane.showMessageDialog(this,"Customer added.","Update",JOptionPane.INFORMATION_MESSAGE);
        System.out.println("Customer created and details updated");
        //clearing fields
        customerNameJTextField.setText("");
        addressJTextField.setText("");
        cityJTextField.setText("");
        stateJTextField.setText("");
        countryJTextField.setText("");
        zipJTextField.setText("");
        populateComboboxes();
        populateOrganizationComboBox();
        emailJTextField.setText("");
        phoneNumberJTextField.setText("");
        customerIDJTextField.setText("");
        
    }//GEN-LAST:event_addCustomerJButtonActionPerformed

