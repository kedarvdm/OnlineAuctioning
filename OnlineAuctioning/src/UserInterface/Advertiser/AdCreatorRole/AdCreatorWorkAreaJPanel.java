    private void assignToMeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignToMeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = orgAdRequestsTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Work Request.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        AdCreationWorkRequest myWorkRequest = (AdCreationWorkRequest) orgAdRequestsTable.getValueAt(selectedRow, 0);
        if(myWorkRequest.getReceiver()!=null)
        {
            JOptionPane.showMessageDialog(this, "Request is already assigned to "+myWorkRequest.getReceiver().getPerson().getName(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        myWorkRequest.setReceiver(userAccount);
        myWorkRequest.setStatus("Under Review");
        myWorkRequest.setResolveDate(new Date());
        System.out.println("Request assigned to "+userAccount.getUsername());
        populateOrgRequestsTable();
        populateMyRequestsTable();
    }//GEN-LAST:event_assignToMeJButtonActionPerformed

    private void createEditAdJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEditAdJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = myAdRequestsTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Work Request.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        AdCreationWorkRequest myWorkRequest = (AdCreationWorkRequest) myAdRequestsTable.getValueAt(selectedRow, 0);
        ProcessAdCreationRequestJPanel piacrjp = new ProcessAdCreationRequestJPanel(userProcessContainer,myWorkRequest,filePath);
        userProcessContainer.add("ProcessImageAdCreationRequestJPanel", piacrjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_createEditAdJButtonActionPerformed

