    private void populateMyRequestsTable()
    {
        DefaultTableModel dtm= (DefaultTableModel) myAdRequestsTable.getModel();
        dtm.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
            if (request instanceof AdCreationWorkRequest) {
                Object row[] = new Object[9];

                row[0] = request;
                row[1] = request.getSender();
                row[2] = request.getReceiver() == null ? null : request.getReceiver().getPerson().getName();
                row[3] = request.getStatus();
                row[4] = request.getRequestDate();
                row[5] = request.getResolveDate();
                row[6] = ((AdCreationWorkRequest)request).getAdvertisement();
                row[7] = ((AdCreationWorkRequest)request).getAdvertisement().isReady()== Boolean.TRUE? "Yes":"No";
                row[8] = ((AdCreationWorkRequest)request).getAdvertisement().isEnabled()== Boolean.TRUE? "Yes":"No";
                dtm.addRow(row);
            }
        }
    }

