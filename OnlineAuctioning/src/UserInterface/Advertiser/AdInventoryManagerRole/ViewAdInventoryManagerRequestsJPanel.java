    private void markAdReadyJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_markAdReadyJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = myAdRequestsTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Work Request.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        AdCreationWorkRequest myWorkRequest = (AdCreationWorkRequest) myAdRequestsTable.getValueAt(selectedRow, 0);
        
        Advertisement advertisement= myWorkRequest.getAdvertisement();
        
        if(advertisement.isReady())
        {
            JOptionPane.showMessageDialog(this,"Advertise is already marked Ready","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!(myWorkRequest.getStatus().equals("Completed")))
        {
            JOptionPane.showMessageDialog(this,"Advertisement is not ready yet","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        advertisement.setReady(true);
        System.out.println("Advertisement marked ready");
        populateMyRequestsTable();
    }//GEN-LAST:event_markAdReadyJButtonActionPerformed

    private void markAdEnabledJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_markAdEnabledJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = myAdRequestsTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Work Request.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        AdCreationWorkRequest myWorkRequest = (AdCreationWorkRequest) myAdRequestsTable.getValueAt(selectedRow, 0);
        
        Advertisement advertisement= myWorkRequest.getAdvertisement();
        
        if(advertisement.isEnabled())
        {
            JOptionPane.showMessageDialog(this,"Advertise is already marked Enabled","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!(myWorkRequest.getStatus().equals("Completed")))
        {
            JOptionPane.showMessageDialog(this,"Advertisement is not ready yet","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!advertisement.isReady() && (myWorkRequest.getStatus().equals("Completed")))
        {
            JOptionPane.showMessageDialog(this,"Please mark advertise as Ready first.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        advertisement.setEnabled(true);
        System.out.println("Advertisement marked enabled");
        populateMyRequestsTable();
    }//GEN-LAST:event_markAdEnabledJButtonActionPerformed

