    private void DisplayAdDetails()
    {
        advertisementIDJTextField.setText(String.valueOf(advertisement.getAdvertisementID()));
        titleJTextField.setText(advertisement.getTitle());
        widthJTextField.setText(String.valueOf(advertisement.getWidth()));
        heightJTextField.setText(String.valueOf(advertisement.getHeight()));
        locationJTextField.setText(advertisement.getLocation());
        dateCreatedJTextField.setText(advertisement.getDateCreated().toString());
        if(advertisement.isReady())
        {
            isReadyYesJRadioButton.setSelected(true);
            isReadyNoJRadioButton.setSelected(false);
        }
        else
        {
            isReadyYesJRadioButton.setSelected(false);
            isReadyNoJRadioButton.setSelected(true);
        }
        
        if(advertisement.isEnabled())
        {
            isEnabledYesJRadioButton.setSelected(true);
            isEnabledNoJRadioButton.setSelected(false);
        }
        else
        {
            isEnabledYesJRadioButton.setSelected(false);
            isEnabledNoJRadioButton.setSelected(true);
        }
        notesJTextField.setText(advertisement.getNotes());
        adWordsJTextField.setText(advertisement.getAdWords());
    }

