    private Boolean verifyFields()
    {
        if(titleJTextField.getText().length()==0
            || heightJTextField.getText().length()==0
            || widthJTextField.getText().length()==0
            || adWordsJTextField.getText().length()==0)
        {
            return false;
        }
        return true;
    }
    
    private void saveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveJButtonActionPerformed
        // TODO add your handling code here:
        if(!verifyFields())
        {
            JOptionPane.showMessageDialog(this,"Please enter valid inputs","Update",JOptionPane.ERROR_MESSAGE);
            return;
        }
        advertisement.setTitle(titleJTextField.getText());
        advertisement.setWidth(Integer.parseInt(widthJTextField.getText()));
        advertisement.setHeight(Integer.parseInt(heightJTextField.getText()));
        if(isReadyYesJRadioButton.isSelected())
        {
            advertisement.setReady(true);
        }
        else if(isReadyNoJRadioButton.isSelected())
        {
            advertisement.setReady(false);
        }
        
        if(isEnabledYesJRadioButton.isSelected())
        {
            advertisement.setEnabled(true);
        }
        else if(isEnabledNoJRadioButton.isSelected())
        {
            advertisement.setEnabled(false);
        }
        advertisement.setAdWords(adWordsJTextField.getText());
        advertisement.setNotes(notesJTextField.getText());
        System.out.println("Advertisement updated");
        JOptionPane.showMessageDialog(this,"Advertisement updated!!","Update",JOptionPane.INFORMATION_MESSAGE);
        saveJButton.setEnabled(false);
        updateJButton.setEnabled(true);
        titleJTextField.setEnabled(false);
        heightJTextField.setEnabled(false);
        widthJTextField.setEnabled(false);
        isReadyYesJRadioButton.setEnabled(false);
        isReadyNoJRadioButton.setEnabled(false);
        isEnabledYesJRadioButton.setEnabled(false);
        isEnabledNoJRadioButton.setEnabled(false);
        adWordsJTextField.setEnabled(false);
        notesJTextField.setEnabled(false);
    }//GEN-LAST:event_saveJButtonActionPerformed


