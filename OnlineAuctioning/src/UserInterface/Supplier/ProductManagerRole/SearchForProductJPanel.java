    private void searchProductJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchProductJButtonActionPerformed
        // TODO add your handling code here:
        int productId = 0;
        try {
            productId = Integer.parseInt(productIDJTextField.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter correct value", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Product product = supplierEnterprise.getProductCatalog().searchProduct(productId);
        if (product != null) {
            ViewProductDetailJPanel vpdjp = new ViewProductDetailJPanel(userProcessContainer, product);
            userProcessContainer.add("ViewProductDetailJPanelSupplier", vpdjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(this, "Product does not exists", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchProductJButtonActionPerformed


