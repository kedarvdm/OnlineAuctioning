    private void preShippingStatusJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_preShippingStatusJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = myShippingRequestsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Work Request.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ShippingRequest myWorkRequest = (ShippingRequest) myShippingRequestsJTable.getValueAt(selectedRow, 0);
        if (myWorkRequest.isHasProcessed()) {
            JOptionPane.showMessageDialog(this, "Cannot alter request as already processed.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        myWorkRequest.setStatus("Pre-Shipping");
        myWorkRequest.getOrder().setStatus("Pre-Shipping");
        myWorkRequest.getOrder().setStatusBy(new Date());
        myWorkRequest.setResolveDate(new Date());
        System.out.println("Status updated to Pre-Shipping");
        populateRequestTable();
        populateMyRequestTable();
    }//GEN-LAST:event_preShippingStatusJButtonActionPerformed

