    private void populateRequestTable() {
        DefaultTableModel dtm = (DefaultTableModel) orgShippingRequestsTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ShippingRequest) {
                Object row[] = new Object[7];

                row[0] = request;
                row[1] = ((ShippingRequest) request).getOrder();
                row[2] = request.getSender();
                row[3] = request.getReceiver() == null ? null : request.getReceiver().getPerson().getName();
                row[4] = request.getStatus();
                row[5] = request.getRequestDate();
                row[6] = request.getResolveDate();
                dtm.addRow(row);
            }
        }

    }

