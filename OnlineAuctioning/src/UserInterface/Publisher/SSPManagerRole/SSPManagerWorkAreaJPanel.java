    private void populateAdSpaceInventory()
    {
        DefaultTableModel dtm= (DefaultTableModel) adSpaceInventoryJTable.getModel();
        dtm.setRowCount(0);

        for (AdSpace adSpace : publisherEnterprise.getAdSpaceInventory().getAdSpaceList()) {
            if (adSpace.isReady()) 
            {
                Object row[] = new Object[5];

                row[0] = adSpace.getAdSpaceID();
                row[1] = adSpace;
                row[2] = adSpace.getDateCreated();
                row[3] = adSpace.isEnabled()== Boolean.TRUE ?  "Yes":"No";
                row[4] = adSpace.getNotes();
                dtm.addRow(row);
            }
        }
    }

    private void populateSSPs(AdSpace adSpace) {
        DefaultTableModel dtm = (DefaultTableModel) sspsJTable.getModel();
        dtm.setRowCount(0);

        for (SSP ssp : publisherEnterprise.getSSPDirectory().getSspList()) {
            if(ssp.getAdSpace()==adSpace)
            {
                Object row[] = new Object[7];

                row[0] = ssp;
                row[1] = ssp.getAgeRange().getValue();
                row[2] = ssp.getOccupation().getValue();
                row[3] = ssp.getIncomeRange().getValue();
                row[4] = ssp.getGender().getValue();
                row[5] = ssp.getBasePrice();
                row[6] = ssp.isEnabled() == Boolean.TRUE ? "Yes" : "No";

                dtm.addRow(row);
            }
        }
    }

