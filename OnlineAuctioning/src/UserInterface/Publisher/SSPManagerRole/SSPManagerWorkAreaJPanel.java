    private void removeSSPJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeSSPJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = sspsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a SSP.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SSP ssp= (SSP) sspsJTable.getValueAt(selectedRow, 0);
        AdSpace adSpace= ssp.getAdSpace();
        publisherEnterprise.getAdSpaceInventory().removeAdpace(adSpace);
        System.out.println("SSP removed");
        JOptionPane.showMessageDialog(this, "SSP removed.", "Update", JOptionPane.INFORMATION_MESSAGE);
        populateSSPs(ssp.getAdSpace());
    }//GEN-LAST:event_removeSSPJButtonActionPerformed

    private void enableDisableSSPJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enableDisableSSPJButtonActionPerformed
        // TODO add your handling code here:
        int flag;
        int selectedRow = sspsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a SSP.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SSP ssp= (SSP) sspsJTable.getValueAt(selectedRow, 0);
        
        if(ssp.isEnabled()==true)
        {
            flag = JOptionPane.showConfirmDialog(this, "Are you sure want to disable this SSP?", "Warning", JOptionPane.YES_NO_OPTION);
            if (flag == JOptionPane.YES_OPTION) {
                ssp.setEnabled(false);
                System.out.println("SSP disabled");
            }
        }else if(ssp.isEnabled()==false)
        {
            flag = JOptionPane.showConfirmDialog(this, "Are you sure want to enable this SSP?", "Warning", JOptionPane.YES_NO_OPTION);
            if (flag == JOptionPane.YES_OPTION) {
                ssp.setEnabled(true);
                System.out.println("SSP enabled");
            }
        }
        populateSSPs(ssp.getAdSpace());
    }//GEN-LAST:event_enableDisableSSPJButtonActionPerformed

