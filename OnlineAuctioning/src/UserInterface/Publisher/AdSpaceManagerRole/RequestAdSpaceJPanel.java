    private void requestAdSpaceJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestAdSpaceJButtonActionPerformed
        // TODO add your handling code here:
        
        Organization organization=null;
        for(Organization org:publisherEnterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(org instanceof AdSpaceCreatorOrganization)
            {
                organization=org;
                break;
            }
        }
        
        //Create blank ad;
        AdSpace adSpace= publisherEnterprise.getAdSpaceInventory().createAndAddAdSpace();
        adSpace.setNotes(notesJTextField.getText());
        adSpace.setEnterprice(publisherEnterprise);
        
        //Create Ad creation request
        AdSpaceCreationWorkRequest myWorkRequest= new AdSpaceCreationWorkRequest();
        myWorkRequest.setSender(userAccount);
        myWorkRequest.setRequestDate(new Date());
        myWorkRequest.setMessage(notesJTextField.getText());
        myWorkRequest.setStatus("Sent");
        myWorkRequest.setAdSpace(adSpace);
       
        if (organization!=null){
            organization.getWorkQueue().getWorkRequestList().add(myWorkRequest);
            userAccount.getWorkQueue().getWorkRequestList().add(myWorkRequest);
        }
        System.out.println("Ad Space Creation work request created and added to Ad Space Creator organization");
        JOptionPane.showMessageDialog(this,"Ad Space Creation work request created");
        notesJTextField.setText("");
    }//GEN-LAST:event_requestAdSpaceJButtonActionPerformed


