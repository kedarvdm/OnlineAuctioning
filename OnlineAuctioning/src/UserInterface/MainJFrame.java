    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // Get user name
        String userName = userNameJTextField.getText();
        // Get Password
        char[] passwordCharArray = passwordField.getPassword();
        String password = String.valueOf(passwordCharArray);
        if(userName.length()==0)
        {
            JOptionPane.showMessageDialog(this,"Please enter valid User Name","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(password.length()==0)
        {
            JOptionPane.showMessageDialog(this,"Please enter Password","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String filePath="";
        
        
        //Step1: Check te system UserAccount directory for the user
        UserAccount userAccount= ecoSystem.getUserAccountDirectory().authenticateUser(userName, password);
        Enterprise inEnterprise= null;
        Organization inOrganization=null;
        if(userAccount==null)
        {
            //Step 2: Go inside each network and check each Enterprise
            for(Network network: ecoSystem.getNetworkList())
            {
                //Step 2.1: Check each Enterprise
                for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList())
                {
                    userAccount=enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    if(userAccount==null)
                    {
                        //Check again each organization
                        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList())
                        {
                            userAccount= organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if(userAccount!=null)
                            {
                                inEnterprise=enterprise;
                                inOrganization=organization;
                                filePath="res\\"+network.getName()+"\\"+enterprise.getName()+"\\"+userAccount.getPerson().getName();
                                //checkAndCreateFileSystem(filePath);
                                break;
                            }
                        }
                    }
                    else
                    {
                        inEnterprise=enterprise;
                        break;
                    }
                    if(inOrganization!=null)
                    {
                        break;
                    }
                }
                if(inEnterprise!=null)
                {
                    break;
                }
            }
        }
        if(userAccount==null)
        {
            JOptionPane.showMessageDialog(this,"Invalid credentials","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            System.out.println(userAccount.getUsername()+" logged in to the system.");
            userProcessContainer.removeAll();
            CardLayout layout= (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("WorkArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, ecoSystem,filePath));
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_loginJButtonActionPerformed

    private void checkAndCreateFileSystem(String filePath)
    {
        File f= new File(filePath);
        if(!(f.exists() && f.isDirectory()))
        {
            f.mkdirs();
        }
    }
